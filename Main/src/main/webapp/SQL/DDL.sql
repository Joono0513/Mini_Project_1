(
  PRODUCT_ID VARCHAR2(100 BYTE) NOT NULL 
, NAME VARCHAR2(200 BYTE) 
, PRICE NUMBER 
, CATEGORY VARCHAR2(100 BYTE) 
, DESCRIPTION VARCHAR2(2000 BYTE) 
, IMAGE_PATH VARCHAR2(1000 BYTE) 
, CONSTRAINT MENU_PK PRIMARY KEY 
  (
    PRODUCT_ID 
