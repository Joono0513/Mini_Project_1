-- 제품 정보 
-- PRODUCT_ID = 제품 번호, NAME = 제품 명, PRICE = 가격, CATEGORY = 제품 분류 카테고리
-- DESCRIPTION = 제품 설명, IMAGE_PATH = 제품 이미지 저장 경로
